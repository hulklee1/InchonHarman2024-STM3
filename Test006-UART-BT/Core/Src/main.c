void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart == &huart1)  // address of handle
	{
		Buf1->v0[idx1++] = dum1;
		HAL_UART_Receive_IT(&huart1, &dum1, 1);
	}
	else if(huart == &huart2)  // KBD input by Putty
	{
		if(dum2 == '\r')	// [Enter] Key
		{
			HAL_UART_Transmit(&huart2, "\r\n", 2, 10);  // Echo
			HAL_UART_Transmit(&huart1, Buf2, idx2, 100);  // redirection to BT
			switch(eol)
			{
			case 1:
				HAL_UART_Transmit(&huart1, "\r\n", 2, 10);	// CRLF
				break;
			case 2:
				HAL_UART_Transmit(&huart1, "\n", 1, 10);	// LF
				break;
			case 3:
				HAL_UART_Transmit(&huart1, "\0", 1, 10);	// NULL
				break;
			case 4:
				//HAL_UART_Transmit(&huart1, "\n\r", 2, 10);	// NONE
				break;
			}
			idx2 = 0;
		}
		else
		{
			HAL_UART_Transmit(&huart2, &dum2, 1, 10);	// Echo
			Buf2->v0[idx2++] = dum2;
			Buf2->v0[idx2] = 0;	// Null
		}
		HAL_UART_Receive_IT(&huart2, &dum2, 1);
	}
}

